pub struct Emojis(phf::Map<&'static str, &'static str>);

impl Emojis {
    pub fn emojis(&self, name: &str) -> Vec<&'static str> {
        let keys = self
            .0
            .keys()
            .filter(|k| k.starts_with(name))
            .collect::<Vec<&&str>>();
        keys.iter().filter_map(|k| self.0.get(k)).copied().collect()
    }
}

